import UIKit

extension UIFont {
    
    class func avenirMedium(size: CGFloat) -> UIFont{
        return UIFont(name: "Avenir-Medium", size: size)!
    }
    
    class func avenirBook(size: CGFloat) -> UIFont{
        return UIFont(name: "Avenir-Book", size: size)!
    }
    
    class func arialRegular(size: CGFloat) -> UIFont{
        return UIFont(name: "ArialMT", size: size)!
    }
    
    class func arialBold(size: CGFloat) -> UIFont{
        return UIFont(name: "Arial-BoldMT", size: size)!
    }
    
}
